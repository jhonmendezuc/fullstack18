--CONTAR LA CANTIDAD DE TAREAS POR USUARIO --COUNT
SELECT U.NAME, COUNT(*)
FROM TASKS T, USERS U
WHERE
    T.USER_ID = U.ID
GROUP BY
    U.NAME;

